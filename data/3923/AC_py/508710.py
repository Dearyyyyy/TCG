# coding=utf-8
while True:
    n=input()
    ay=0
    an=0
    by=0
    bn=0
    cy=0
    cn=0
    R=0
    A=0
    S=0
    B=0
    T=0
    ay=n.count("1Y")
    an=n.count("1N")
    by=n.count("2Y")
    bn=n.count("2N")
    cy=n.count("3Y")
    cn=n.count("3N")
    R=n.count("R")
    A=n.count("A")
    S=n.count("S")
    B=n.count("B")
    T=n.count("T")
    score=ay+by*2+cy*3
    shout=bn+cn
    value=(score+R+A+S+B)-shout-an-T
    print(value)